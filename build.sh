# Basic platform handler ( http://stackoverflow.com/a/18434831 ).

# ------------------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------------------

case "$OSTYPE" in
    solaris*)           echo "SOLARIS" ;;
    darwin*)            ./buildMac.sh ;;
    linux-gnueabihf)    ./buildRaspbian.sh ;;
    linux*)             ./buildDebian.sh ;;
    bsd*)               echo "BSD" ;;
    *)                  echo "unknown: $OSTYPE" ;;
esac

