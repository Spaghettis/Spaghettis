case "$OSTYPE" in
    solaris*) echo "SOLARIS" ;;
    darwin*)  ./buildMacOS.sh ;;
    linux*)   ./buildDebian.sh ;;
    bsd*)     echo "BSD" ;;
    *)        echo "unknown: $OSTYPE" ;;
esac

