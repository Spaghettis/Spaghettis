static t_class *key_class;          /* Shared. */

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

typedef struct _key {
    t_object    x_obj;              /* Must be the first. */
    int         x_edit;
    t_outlet    *x_outlet;
    } t_key;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static void key_float (t_key *x, t_float f)
{
    if (x->x_edit || !instance_hasOpenedWindowInEditMode()) { outlet_float (x->x_outlet, f); }
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static void *key_new (t_symbol *s, int argc, t_atom *argv)
{
    t_key *x = (t_key *)pd_new (key_class);
    
    if (argc && atom_getSymbol (argv) == sym___dash__editmode) { x->x_edit = 1; argc--; argv++; }

    error__options (s, argc, argv);
    
    if (argc) { warning_unusedArguments (s, argc, argv); }
    
    x->x_outlet = outlet_newFloat (cast_object (x));
    
    pd_bind (cast_pd (x), sym__key);
    
    return x;
}

static void key_free (t_key *x)
{
    pd_unbind (cast_pd (x), sym__key);
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void key_setup (void)
{
    t_class *c = NULL;
    
    c = class_new (sym_key,
            (t_newmethod)key_new,
            (t_method)key_free,
            sizeof (t_key),
            CLASS_DEFAULT | CLASS_NOINLET,
            A_GIMME,
            A_NULL);
        
    class_addFloat (c, (t_method)key_float);
    
    key_class = c;
}

void key_destroy (void)
{
    class_free (key_class);
}

