static inline double tll_randomGetDouble (t_rand48 *x)
{
    t_rand48 seed = (*x); double f = PD_RAND48_DOUBLE (seed); (*x) = seed; return f;
}

static inline int tll_randomGetInteger (t_rand48 *x, int v)
{
    if (v <= 1) { return 0; } else { return (int)(v * tll_randomGetDouble (x)); }
}

